# BMI Calculater

weight = float(input("Zajdete svou váhu v kg (x.y):\n"))
height = float(input("Zajdete svou výšku v m (x.yy):\n"))
               
bmi = weight/(height*height)
bmi = round(bmi,2)
print(f"Vaše BMI je: {bmi}")                


